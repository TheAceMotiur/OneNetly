  window.importConfiguration = function() {
    const input = document.createElement('input');
    input.type = 'file';
    input.accept = '.json';
    input.onchange = function(e) {
      const file = e.target.files[0];
      if (file) {
        const reader = new FileReader();
        reader.onload = function(e) {
          try {
            const config = JSON.parse(e.target.result);
            
            // Apply configuration
            Object.keys(data.networks).forEach(id => {
              data.networks[id].visible = config.networks ? config.networks.includes(id) : false;
            });
            
            if (config.size) {
              Object.keys(data.sizes).forEach(k => data.sizes[k] = false);
              data.sizes[config.size] = true;
            }
            
            if (config.iconStyle) {
              Object.keys(data.icons).forEach(k => data.icons[k] = false);
              if (config.iconStyle.includes('solid')) data.icons.solid = true;
              if (config.iconStyle.includes('circle')) data.icons.circle = true;
            }
            
            if (config.url) data.url = config.url;
            if (config.text) data.text = config.text;
            
            renderNetworkOptions();
            renderSizeOptions();
            renderIconOptions();
            renderAll();
            
            showNotification('Configuration imported successfully!', 'success');
          } catch (err) {
            showNotification('Invalid configuration file!', 'error');
          }
        };
        reader.readAsText(file);
      }
    };
    input.click();
  };

