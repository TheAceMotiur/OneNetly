<script>
export default {
  name: 'WordPressPlugin',
  mounted() {
    // Add the OneNetly widget to this page for demonstration
    this.loadWidget();
  },
  methods: {
    loadWidget() {
      // Create script element for OneNetly widget
      const script = document.createElement('script');
      script.src = 'https://onenetly.com/widget.js';
      script.setAttribute('data-position', 'bottom-right');
      script.setAttribute('data-networks', 'Facebook,Twitter,LinkedIn,WhatsApp,Telegram,Reddit');
      document.head.appendChild(script);
    }
  }
}
</script>
